#include<stdio.h>

int main()
{
	int iNo1 =10;
	float fValue = 3.14;
	double dData = 6.10;
	char cValue = 'M';
	
	printf("Integer is %d \n", iNo1);
	printf("float is %f \n", fValue);
	printf("double is %lf \n", dData);
	printf("char is %c \n", cValue);

	return 0;
}

/*
%d	Decimal		int
%f	Float		float
%lf	Long Float	double
%c	Character	char
%s	String		string
*/
